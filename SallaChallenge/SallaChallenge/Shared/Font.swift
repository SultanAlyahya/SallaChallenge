import SwiftUI

extension Font {
    static func sallaRegular(size: CGFloat) -> Font {
        return Font.custom("DINNextLTArabic-Regular", size: size)
    }
    static func sallaMedium(size: CGFloat) -> Font {
        return Font.custom("Estedad-Medium", size: size)
    }
}
